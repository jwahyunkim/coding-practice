package edu;

public class Test009_복합대입연산자 {

	public static void main(String[] args) {
		int a = 10;
		int b = 2;
		a+=b;
		System.out.println(a);
		a-=b;
		System.out.println(a);
		a*=b;
		System.out.println(a);
		a/=b;
		System.out.println(a);
		a%=b;
		System.out.println(a);


	}

}
